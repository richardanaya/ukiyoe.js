var Key = {};
Key.keys = [];

Key.isDown = function(keyCode){
    if(Key.keys[keyCode]){
        return true;
    }
    return false;
};

Key.isUp = function(keyCode){
    if(!Key.keys[keyCode]){
        return true;
    }
    return false;
};

document.onkeydown = function(e){
    Key.keys[e.keyCode] = true
};

document.onkeyup = function(e){
    Key.keys[e.keyCode] = false
};

